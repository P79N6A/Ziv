package data.view.homework.consts;

public class ABCDConsts {
	
	public static final String A = "A";
	public static final String B = "B";
	public static final String C = "C";
	public static final String D = "D";
	
	
	public static boolean isA(String answer) {
		return A.equals(answer);
	}
	
	public static boolean isB(String answer) {
		return B.equals(answer);
	}
	
	public static boolean isC(String answer) {
		return C.equals(answer);
	}
	
	public static boolean isD(String answer) {
		return D.equals(answer);
	}

}
